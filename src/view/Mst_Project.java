    private void loadData() {
        List<Model_Project> list = service.getData();
        List<Model_Project> listVendor = service.getDataVendor();
        tblMod.setData(list);
        mainPanel.revalidate();
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        model.addElement("-- Choose Vendor --");

    // Tambahkan nama vendor ke model
    for (Model_Project project : listVendor) {
        model.addElement(project.getMovr().getVendor_name().toString());
    }
    
    // Set model ke JComboBox
    cbVendor.setModel(model);
    }
    public String generateSmartId(String prefix) {
        String date = new SimpleDateFormat("yyyyMMdd").format(new Date());
        int random = new Random().nextInt(9000) + 1000; // 4 digit random
        return prefix + date + random;
    }

