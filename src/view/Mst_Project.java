    private void saveData() {
        if(inputValidation()){
            String pn = ProjectName.getText();
            String ps = ProjectSite.getText();
            String pv = (String) cbVendor.getSelectedItem();
            String pm = (String) cbPm.getSelectedItem();
            String sp = (String) cbSp.getSelectedItem();
            
            
            Model_Project mp = new Model_Project();
            Model_Vendor mv = new Model_Vendor();
            mp.setId_project(generateSmartId(getFourthChar((String) cbVendor.getSelectedItem())+"-"));
            mp.setProject_manager(pm);
            mp.setProject_name(pn);
            mp.setStatus(sp);
            mp.setProject_site(ps);  
            List<Model_Project> listVendor = service.getDataVendor();
            for (Model_Project project : listVendor) {
                    if(project.getMovr().getVendor_name().toString().equals(pv)){
                        mv.setId_vendor(project.getMovr().getId_vendor().toString());
                        mp.setMovr(mv);
                       
                        
                    }
                }
            mp.setSts_active("1");
            mp.setCreated_date(getCurrentDateTime());
            mp.setUpdated_date(getCurrentDateTime());
            mp.setUpdated_by("Admin");
            mp.setCreated_by("Admin");
//            System.out.print((String)generateSmartId(getFourthChar((String) cbVendor.getSelectedItem())+"-"));
            service.addData(mp);
            tblMod.addData(mp);
            resetForm();
            loadData();
            showPanel();
        }
    }
    public String getCurrentDateTime() {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        return now.format(formatter);
    }
    public String getFourthChar(String text) {
        if (text != null && text.length() >= 4) {
          return String.valueOf(text.charAt(3)); // huruf ke-4 (indeks ke-3)
        } else {
           throw new IllegalArgumentException("String harus memiliki minimal 4 karakter.");
        }
    }
    private boolean inputValidation(){
        boolean result = false;
        if(ProjectName.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Project Name is Required!");
        }else if(ProjectSite.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Project Site is Required!");
        }else if(cbVendor.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Vendor Name is Required!");
        }else if(cbPm.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Project Manager is Required!");
        }else if(cbSp.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Status Project is Required!");
        }else{
            result = true;
        }
        return result;
    }

