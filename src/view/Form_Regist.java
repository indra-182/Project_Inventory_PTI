    private void proccessRegist() {
        if(validationRegist()){
            view.Mst_Project mp = new Mst_Project();
            String username = tfUser.getText();
            String pass = new String(tfPw.getPassword());
            String idKry = tfIdKry.getText();
            model.Model_Login mod_log = new Model_Login();
            model.Model_Group mod_group = new Model_Group();
            model.Model_Karyawan mod_kry = new Model_Karyawan();
            mod_log.setUsername(username);
            mod_log.setPassword(pass);
            mod_group.setId_group("G-STF");
            mod_group.setGroup_name("Staff");
            mod_log.setMogr(mod_group);
//            mod_log.setMogr(mod_group).setId_Group("G-SPA");
            mod_kry.setId_karyawan(idKry);
//            mod_kry.setGroup_name("Super Admin");
            mod_log.setMokr(mod_kry);
            mod_log.setCreated_by(username);
            mod_log.setCreated_date(mp.getCurrentDateTime());
            mod_log.setUpdated_date(mp.getCurrentDateTime());
            mod_log.setLast_active(mp.getCurrentDateTime());
            mod_log.setUpdated_by("Super Admin");
//            mod_log.setId_karyawan(mp.generateSmartId("KRY-"));
            Form_login fl = new Form_login();
            Form_Regist fr = new Form_Regist();
            service.registLogin(mod_log,fl,fr);
            tfUser.setText("");
            tfPw.setText("");
            tfIdKry.setText("");
            dispose();
        }
    }

    
