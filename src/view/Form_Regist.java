    private boolean validationRegist(){
      boolean result = false;
      String username = tfUser.getText(); 
      String pass = new String(tfPw.getPassword());
      String idKry = tfIdKry.getText(); 
      if (username.trim().isEmpty() && pass.trim().isEmpty()){
        JOptionPane.showMessageDialog(null,"Username & Password is required");
      }
        else if (username.trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Username is required");
      } else if (pass.trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Password is required");
      }else if (idKry.trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"ID Karyawan is required");
      }else{
            result = true;      
      }    
        return result;
    };
    
