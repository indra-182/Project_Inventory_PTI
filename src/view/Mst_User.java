    private void saveData() {
        if(inputValidation()){
            String pn = Username.getText();
            String ps = new String(tfPw.getPassword());
            String pv = (String) ck.getSelectedItem();
            String pv1 = (String) ck1.getSelectedItem();
            
            
            Model_User mp = new Model_User();
            Model_Group mp1 = new Model_Group();
            Model_Karyawan mv = new Model_Karyawan();
            mp.setUsername(pn);
            mp.setPassword(ps);
            
            List<Model_User> listKaryawan = service.getDataKaryawan();
            for (Model_User karyawan : listKaryawan) {
                    if(karyawan.getMovr().getNama_lengkap().toString().equals(pv)){
                        mp.setId_karyawan(karyawan.getMovr().getId_karyawan().toString());
                        mp.setMovr(mv);
                       
                        
                    }
                }
            List<Model_User> listGroup = service.getDataGroup();
            for (Model_User karyawan : listGroup) {
                    if(karyawan.getMogr().getGroup_name().toString().equals(pv1)){
                        mp1.setId_group(karyawan.getMogr().getId_group().toString());
                        mp.setMogr(mp1);
                       
                        
                    }
                }
            
            mp.setSts_active("1");
            mp.setCreated_date(getCurrentDateTime());
            mp.setUpdated_date(getCurrentDateTime());
            mp.setUpdated_by("Admin");
            mp.setCreated_by("Admin");
            mp.setLast_login(getCurrentDateTime());
//            System.out.print((String)generateSmartId(getFourthChar((String) cbVendor.getSelectedItem())+"-"));
            service.addData(mp);
            tblMod.addData(mp);
            resetForm();
            loadData();
            showPanel();
        }
    }
    public String getCurrentDateTime() {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        return now.format(formatter);
    }
    public String getFourthChar(String text) {
        if (text != null && text.length() >= 4) {
          return String.valueOf(text.charAt(3)); // huruf ke-4 (indeks ke-3)
        } else {
           throw new IllegalArgumentException("String harus memiliki minimal 4 karakter.");
        }
    }
    private boolean inputValidation(){
        boolean result = false;
        if(Username.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"UserName is Required!");
        }else if(tfPw.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Password is Required!");
            }else if(ck.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Karyawan is Required!");
        }else{
            result = true;
        }
        return result;
    }

