     private void loadData() {
        List<Model_User> list = service.getData();
        List<Model_User> listKaryawan = service.getDataKaryawan();
        List<Model_User> listGroup = service.getDataGroup();
        tblMod.setData(list);
        mainPanel.revalidate();
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        model.addElement("-- Pilih Karyawan --");

    // Tambahkan nama vendor ke model
    for (Model_User karyawan : listKaryawan) {
        model.addElement(karyawan.getMovr().getNama_lengkap().toString());
    }
    
    // Set model ke JComboBox
    ck.setModel(model);
            DefaultComboBoxModel<String> model1 = new DefaultComboBoxModel<>();
        model1.addElement("-- Pilih Group --");

    // Tambahkan nama vendor ke model
    for (Model_User group : listGroup) {
        model1.addElement(group.getMogr().getGroup_name().toString());
    }
    
    // Set model ke JComboBox
    ck1.setModel(model1);
    }
    public String generateSmartId(String prefix) {
        String date = new SimpleDateFormat("yyyyMMdd").format(new Date());
        int random = new Random().nextInt(9000) + 1000; // 4 digit random
        return prefix + date + random;
    }

    private void resetForm() {
        Username.setText("");
        tfPw.setText("");
        
    }

    private void showPanel() {
        mainPanel.removeAll();
        mainPanel.add(new Mst_User());
        mainPanel.repaint();
        mainPanel.revalidate();
    }

    private void getDataTable() {
        int row = dataUserTable.getSelectedRow();
        Username.setText(dataUserTable.getModel().getValueAt(row, 3).toString());
        tfPw.setText(dataUserTable.getModel().getValueAt(row, 4).toString());
        ck.setSelectedItem((String) dataUserTable.getModel().getValueAt(row, 1).toString());
        ck1.setSelectedItem((String) dataUserTable.getModel().getValueAt(row, 2).toString());
        
    }
    
    private void updateData() {
        int index = dataUserTable.getSelectedRow();
        if(index!=-1){
//            Model_Project modPro = tblMod.getData(dataProjectTable.convertRowIndexToModel(index));
            if(inputValidation()){
                String ip = dataUserTable.getModel().getValueAt(index, 3).toString();
                String pv1 = (String) ck1.getSelectedItem();
                String ps = tfPw.getText();
                
                Model_User mp = new Model_User();
                Model_Group mg = new Model_Group();
                List<Model_User> listGroup = service.getDataGroup();
            for (Model_User karyawan : listGroup) {
                    if(karyawan.getMogr().getGroup_name().toString().equals(pv1)){
                        mg.setId_group(karyawan.getMogr().getId_group().toString());
                        mp.setMogr(mg);
                       
                        
                    }
                }
                
                mp.setUsername(ip);
                mp.setPassword(ps);
                mp.setUpdated_date(getCurrentDateTime());
                mp.setUpdated_by("Super Admin");
                service.updateData(mp);
                tblMod.updateData(index, mp);
                loadData();
                showPanel();
            }
        }
    }

    private void delData() {
        int index = dataUserTable.getSelectedRow();
        if(index!=-1){
            Model_User modPro = tblMod.getData(dataUserTable.convertRowIndexToModel(index));
            if(JOptionPane.showConfirmDialog(null,"Delete this Data?","Warning",JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION){
                service.deleteData(modPro);
                tblMod.deleteData(index);
                loadData();
                resetForm();
            }
        }else{
            JOptionPane.showMessageDialog(null, "No data selected. Please select a row to delete!");
        }
    }
    
